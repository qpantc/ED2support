#------------------------------------------------------------------------------------------#
get.os <<- function(){
   if (.Platform$OS.type %in% "windows"){
      ans = "windows"
   }else if (Sys.info()["sysname"] %in% "Darwin"){
      ans = "macos"
   }else if (.Platform$OS.type %in% "unix"){
      ans = "linux"
   }else{
      stop("Unknown OS")
   }#end if 
   return(ans)
}#end function get.os (.Platform$OS.type %in% "windows")
